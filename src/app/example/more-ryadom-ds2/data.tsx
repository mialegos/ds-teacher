export function Avatar({
  initials,
  size = 32,
  className = "",
}: {
  initials: string;
  size?: number;
  className?: string;
}) {
  const src = AVATAR_MAP[initials];
  if (!src) {
    return (
      <div
        className={`flex items-center justify-center rounded-full bg-accent text-xs font-semibold text-accent-foreground ${className}`}
        style={{ width: size, height: size }}
      >
        {initials}
      </div>
    );
  }
  return (
    <img
      src={src}
      alt={initials}
      width={size}
      height={size}
      className={`rounded-full object-cover ${className}`}
      style={{ width: size, height: size }}
    />
  );
}

/* ── Helpers ── */

export function scoreColor(score: number) {
  if (score >= 6.5) return "text-green-ui-500";
  if (score >= 4.0) return "text-orange-500";
  return "text-red-500";
}

export function statusVariant(status: string) {
  if (status === "Бронирование") return "success" as const;
  if (status === "В работе") return "warning" as const;
  return "secondary" as const;
}

const MONTHS_SHORT = ["янв.", "февр.", "мар.", "апр.", "мая", "июн.", "июл.", "авг.", "сент.", "окт.", "нояб.", "дек."];

export function fmtDate(iso: string) {
  const [y, m, d] = iso.split("-");
  return `${Number(d)} ${MONTHS_SHORT[Number(m) - 1]} ${y}`;
}

